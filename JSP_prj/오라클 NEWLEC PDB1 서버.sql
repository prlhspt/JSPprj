CREATE TABLE MEMBER
(
ID      VARCHAR2(50),
PWD     NVARCHAR2(50),
NAME    NVARCHAR2(50),
GENDER  NCHAR(2),
AGE     NUMBER,
BIRTHDAY CHAR(10 BYTE),
PHONE   CHAR(13 BYTE),
REGDATE DATE
);

